package modelo;

public class EspecialidadDoctor{
    private String codEspe, nomEspe;
    private double tarifa;
    

    
    

    public String getCodEspe() {
        return codEspe;
    }

    public void setCodEspe(String codEspe) {
        this.codEspe = codEspe;
    }

    public String getNomEspe() {
        return nomEspe;
    }

    public void setNomEspe(String nomEspe) {
        this.nomEspe = nomEspe;
    }

    public double getTarifa() {
        return tarifa;
    }

    public void setTarifa(double tarifa) {
        this.tarifa = tarifa;
    }
    
    
    
}
